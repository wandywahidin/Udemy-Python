# Perulangan dengan for adalah perulangan yang diketahui akan berapa kali dilakukan perulangan nya

jumlah_buku = 40
print(f'jummlah buku yang ada {jumlah_buku} buku')
jumlah_buku_yang_dibaca = 0
for jumlah_buku_yang_dibaca in range(jumlah_buku):
    print(f'buku ke {jumlah_buku_yang_dibaca+1} sudah dibaca')


